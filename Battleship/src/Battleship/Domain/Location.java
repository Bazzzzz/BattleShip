package Battleship.Domain;

/**
 *
 * @author sebas
 */
public class Location {
    private int xCoord;
    private int yCoord;

    public Location(int xCoord, int yCoord) {
        this.xCoord = xCoord;
        this.yCoord = yCoord;
    }

    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }
    
    
}
